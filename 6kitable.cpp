#include<bits/stdc++.h>
using namespace std;

int main(){
    for(int i =1; i<=10; i++){
        cout<<i*6<<"\n";
    }
    cout<<"\n"<<"\n";

    int j = 1;
    while(j<=10){
        cout<<j*27<<"\n";
        j++;
    }
    cout<<"\n"<<"\n";

    int p = 1;
    do{
        cout<<p*78<<"\n";
        p++;
    }while(p<=10);

}